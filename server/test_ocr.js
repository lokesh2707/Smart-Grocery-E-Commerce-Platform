const Tesseract = require('tesseract.js');
const fs = require('fs');
const path = require('path');

async function testOCR() {
  try {
    console.log('Testing Tesseract.js...');
    console.log('Tesseract module loaded:', typeof Tesseract);
    
    // Try to create a simple test image (1x1 white pixel PNG)
    const buffer = Buffer.from([
      0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D,
      0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
      0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53, 0xDE, 0x00, 0x00, 0x00,
      0x0C, 0x49, 0x44, 0x41, 0x54, 0x08, 0x99, 0x63, 0xF8, 0xCF, 0xC0, 0x00,
      0x00, 0x00, 0x03, 0x00, 0x01, 0x89, 0x2B, 0x3A, 0x6E, 0x00, 0x00, 0x00,
      0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82
    ]);

    console.log('Testing with minimal image buffer...');
    
    const result = await Tesseract.recognize(buffer, 'eng', {
      logger: (m) => console.log('Tesseract progress:', m.status, m.progress ? Math.round(m.progress * 100) + '%' : '')
    });

    console.log('OCR Success! Text:', result.data.text);
  } catch (error) {
    console.error('OCR Error:', error.message);
    console.error('Full error:', error);
    console.error('Stack:', error.stack);
  }
}

testOCR();
